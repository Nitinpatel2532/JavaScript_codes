// target HTML elements and perform operations
// Events,queryselector,classList


// classList add()
function cnz() {
    document.querySelector(".div1").classList.add('div1css')
    
}

// classList remove()
function cnz2() {
    document.querySelector(".div2").classList.remove('div2css')
    
}

// classList toggle()
function cnz3() {
    document.querySelector(".div3").classList.toggle('div1css')
    
}